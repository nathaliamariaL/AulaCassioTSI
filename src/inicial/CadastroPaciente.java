   public boolean validarFormulario(){
      
        
        if(rg_paci.getText().isEmpty()){
             JOptionPane.showMessageDialog(this, "Preencha o campo Rg.", "Alerta", JOptionPane.WARNING_MESSAGE);
             rg_paci.requestFocus();
             return false;
        
        }
        if(NOME_PACI.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Preencha o campo Nome.", "Alerta", JOptionPane.WARNING_MESSAGE);
             NOME_PACI.requestFocus();
             return false;
        }
                if(END_PACI.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Preencha o campo Endereço.", "Alerta", JOptionPane.WARNING_MESSAGE);
             END_PACI.requestFocus();
             return false;
        }
                
            if(TEL_PACI.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Preencha o campo Telefone.", "Alerta", JOptionPane.WARNING_MESSAGE);
             TEL_PACI.requestFocus();
             return false;
        }
                            
            if(EMAIL_PACI.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Preencha o campo E-mail.", "Alerta", JOptionPane.WARNING_MESSAGE);
             EMAIL_PACI.requestFocus();
             return false;
        }
       
    
            if (rg_paci.getText().trim().length() < 2) {
            JOptionPane.showMessageDialog(this, "RG do paciente inválido.", "Alerta", JOptionPane.WARNING_MESSAGE);
            rg_paci.requestFocus();
            return false;
        }
            
           if (NOME_PACI.getText().trim().length() < 2) {
            JOptionPane.showMessageDialog(this, "Nome do paciente inválido.", "Alerta", JOptionPane.WARNING_MESSAGE);
            NOME_PACI.requestFocus();
            return false;
        }
           
                       
           if (END_PACI.getText().trim().length() < 2) {
            JOptionPane.showMessageDialog(this, "Endereço do paciente inválido.", "Alerta", JOptionPane.WARNING_MESSAGE);
            END_PACI.requestFocus();
            return false;
        }
                                  
           if (TEL_PACI.getText().trim().length() < 2) {
            JOptionPane.showMessageDialog(this, "Telefone do paciente inválido.", "Alerta", JOptionPane.WARNING_MESSAGE);
            TEL_PACI.requestFocus();
            return false;
        }
                   if (EMAIL_PACI.getText().trim().length() < 2) {
            JOptionPane.showMessageDialog(this, "E-mail do paciente inválido.", "Alerta", JOptionPane.WARNING_MESSAGE);
            EMAIL_PACI.requestFocus();
            return false;
        }
    
         return true;
    }
    
 
    
