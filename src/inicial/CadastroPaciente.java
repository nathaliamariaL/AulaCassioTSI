    private void CADASTRO_PACActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CADASTRO_PACActionPerformed
       if (validarFormulario()) {
        
            paci = new Paciente();
            paci.setNome_paci(NOME_PACI.getText().trim());
            paci.setRg_paci(rg_paci.getText().trim());
            paci.setEndereco_paci(END_PACI.getText().trim());
            paci.setTelefone_paci(Integer.parseInt(TEL_PACI.getText().trim()));
            paci.setEmail_paci(EMAIL_PACI.getText().trim());
           
            //parseDouble() converte o texto para double

          
                try {
                    paciDAO.inserir(paci);
                    //mensagem em caso de sucesso
                    
                    JOptionPane.showMessageDialog(this,"Dados Inseridos com sucesso");
                    
                    id = id + 1; 
                    //increment do id
                    
                     ID_PACI.setText(String.valueOf(id));
                    //retorna o valor do parametro como String
                        NOME_PACI.setText("");
                        rg_paci.setText("");
                        END_PACI.setText("");
                        TEL_PACI.setText("");
                        EMAIL_PACI.setText("");      
                     //limpando os campos após o cadastro
                  
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Erro ao cadastrar paciente.\n" + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
                    //mensagem informando qual erro ocorreu
                    return;
                }    
       }
    }//GEN-LAST:event_CADASTRO_PACActionPerformed

